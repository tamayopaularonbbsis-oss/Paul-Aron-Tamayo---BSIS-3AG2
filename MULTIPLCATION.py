#MULTIPLYING TWO NUMBERS FROM USER INPUT

while True:
    print("=================================")
    number_input1 = int(input("Enter first number: "))
    number_input2 = int(input("Enter second number: "))
    print("=================================")

    input_product = number_input1 * number_input2

    print(f'The product of numbers is: {input_product}')



